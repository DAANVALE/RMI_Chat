    private void btnCLienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCLienteActionPerformed
        
        if(idClient<5){
            String Ip=JOptionPane.showInputDialog(null, "Connect to: (ip)", "Client");
            username=JOptionPane.showInputDialog(null, "Username", "Client");

            chatClient=new Chat_Client();
            chatClient.startClient(username, textArea,Ip);
            title.setText("Cliente: " + username);
            
            idClient++;
            textArea.append("Connected to the Server: " + Ip );
            System.out.println(idClient);
            
            this.btnPrivado.setVisible(true);
            this.btnEnviar.setVisible(true);
            this.jScrollPane1.setVisible(true);
            this.txtMensaje.setVisible(true);
            
            this.btnCLiente.setVisible(false);
            this.btnServidor.setVisible(false);
        
            this.setSize(410,350);
        }             
    }//GEN-LAST:event_btnCLienteActionPerformed

    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if(!txtMensaje.getText().isEmpty())
        {
            String message=txtMensaje.getText();

            try {
                chatClient.sendMessage(message,username);
                System.out.println(idClient-1);
                cleanTxt();
            } catch (RemoteException ex) {
                Logger.getLogger(GUI_Main.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }

    }//GEN-LAST:event_btnEnviarActionPerformed

    private void btnPrivadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrivadoActionPerformed
        if(!txtMensaje.getText().isEmpty()){

            String privateUser=JOptionPane.showInputDialog(rootPane, "Whisper to:", "Client", HEIGHT);
            try {
                chatClient.sendPrivate(username, privateUser, txtMensaje.getText());
                cleanTxt();
            } catch (RemoteException ex) {
                Logger.getLogger(GUI_Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPrivadoActionPerformed

    private void cleanTxt(){
        txtMensaje.setText("");
    }
    
